</div>

<script>
    // addToast
    function addToast(){
        toastWrap = document.querySelector(".toast-wrap");
        toastBox = document.createElement("div");
        toastBox.innerHTML = "로그인이 실패하였습니다.";
        toastBox.setAttribute("class", "toast-box Text-md");

        toastWrap.appendChild(toastBox);
    }

    $(document).ready(function(){
        // modal open
        let btnModalOpen = document.querySelectorAll('.btn-modal-open');
        for(let i =0; i<btnModalOpen.length; i++){
            btnModalOpen[i].addEventListener('click',function(){
                let thisName = document.querySelector('#'+this.dataset.name);
                thisName.style.display = 'block';
            });
        };

        // modal close
        let btnModalClose = document.querySelectorAll('.btn-modal-close');
        for(let i =0; i<btnModalClose.length; i++){
            btnModalClose[i].addEventListener('click',function(){
                this.closest('.modal').style.display = 'none';
            });
        };

    })
</script>